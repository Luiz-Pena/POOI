    private void jbEnviarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEnviarDadosActionPerformed
        if (jlEstadoArquivo.getText().equals("Arquivo Aberto!") && !(jtNumeroConta.getText().equals("") ||
                jtPrimeiroNome.getText().equals("") ||
                jtSobrenome.getText().equals("") ||
                jtBalance.getText().equals("") )){
           
            int acc;
            String fname;
            String lname;
            int bal;
            int erro;
            
            acc = Integer.parseInt(jtNumeroConta.getText());
            fname = jtPrimeiroNome.getText();
            lname = jtSobrenome.getText();
            bal = Integer.parseInt(jtBalance.getText());
            
            erro = arquivo.addRecords(acc, fname, lname, bal);
            
            switch (erro) {
                
                case -1:
                    JOptionPane.showMessageDialog(rootPane, "O numero da conta nao pode ser negativo");
                    break;
                   
                case -2:
                    JOptionPane.showMessageDialog(rootPane, "Valores Invalidos");
                    break;
            
                case 1:
                    jtNumeroConta.setText("");
                    jtPrimeiroNome.setText("");
                    jtSobrenome.setText("");
                    jtBalance.setText("");
                    break;
            }
        }
    }//GEN-LAST:event_jbEnviarDadosActionPerformed

