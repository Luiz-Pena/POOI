    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        WriteRandomFile file = new WriteRandomFile();
        
        if (file.openFile() == false) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao abrir o arquivo!");
            System.exit(0);
        }
        
        String numero = jTNumeroConta.getText();
        String nome = jTNome.getText();
        String sobrenome = jTSobrenome.getText();
        String saldo = jTSaldo.getText();
        
        if (index == 99)
            JOptionPane.showMessageDialog(rootPane, "Limite alcan√ßado de registros!");
        else {
       
            if (saldo.isEmpty() || nome.isEmpty() || sobrenome.isEmpty() || numero.isEmpty()) 
                JOptionPane.showMessageDialog(rootPane, "Dados Faltantes!");
        
            else {
                switch (file.writeFile(Integer.parseInt(numero), nome, sobrenome, Double.parseDouble(saldo))) {
                    case -1:
                        JOptionPane.showMessageDialog(rootPane, "O numero da conta deve ser entre 0 e 100!");
                        break;
                
                    case 0:
                        JOptionPane.showMessageDialog(rootPane, "Dados salvos!");
                        
                        index++;
                        jTNumeroConta.setText("");
                        jTNome.setText("");
                        jTSobrenome.setText("");
                        jTSaldo.setText("");
                        break;
                
                    case 1:
                        JOptionPane.showMessageDialog(rootPane, "Erro ao gravar dados!");
                        break;
                
                    case 2:
                        JOptionPane.showMessageDialog(rootPane, "Dados invalidos!");
                        break;
                }
        
            }
        }
        
        if (file.closeFile() == false) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao fechar o arquivo!");
            System.exit(0);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

