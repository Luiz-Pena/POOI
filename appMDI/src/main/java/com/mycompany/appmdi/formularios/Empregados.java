    private void jbListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbListarActionPerformed
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        FormMain form = new FormMain();
        
        Empregado empregados[] = form.getEmpregados();
        Integer index = form.getIndex();
        
        model.setNumRows(0);
        
        for (int count = 0; count < index; count++){
            
 
            if ((jRCLT.isSelected() || jRTodos.isSelected()) && empregados[count] instanceof EmpregadoCLT) {
                EmpregadoCLT temp = (EmpregadoCLT)empregados[count];
                
                model.addRow(new Object[]{
                    temp.getNome(),
                    temp.getEndereco(),
                    temp.getCpf(), 
                    temp.getsBrutoCLT()
                    });
            }
           
            if ((jRHorista.isSelected() || jRTodos.isSelected()) && empregados[count] instanceof EmpregadoHorista) {
                EmpregadoHorista temp = (EmpregadoHorista)empregados[count];
                  
                model.addRow(new Object[]{
                    temp.getNome(),
                    temp.getEndereco(),
                    temp.getCpf(), 
                    "",
                    temp.getnHoras(),
                    temp.getPrecoH()
                    });
            }
        }
         
    }//GEN-LAST:event_jbListarActionPerformed


