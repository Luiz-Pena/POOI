    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        FormMain form = new FormMain();
        
        String nome;
        String cpf;
        String endereco;
        Double sBrutoCLT;
        
        nome = jtNome.getText();
        endereco = jtEndereco.getText();
        cpf = jtCPF.getText();
        sBrutoCLT = Double.parseDouble(jtSalarioBruto.getText());
        
        form.inserirCLT(nome, endereco, cpf, sBrutoCLT);
        
        JOptionPane.showMessageDialog(rootPane, "Registro salvo!");
        
        jtCPF.setText("");
        jtEndereco.setText("");
        jtNome.setText("");
        jtSalarioBruto.setText("");
    }//GEN-LAST:event_jbSalvarActionPerformed

